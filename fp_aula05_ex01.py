# in - is a keyword used to check if a value exists in a sequence (list, tuple, string) or to iterate over a sequence of values.
# range - is a built-in function that generates a sequence of numbers, which can be used in for loops to iterate a specific number of times.
# The for loop is a control flow statement that allows you to iterate over a sequence of values, executing a block of code for each value in the sequence.

for numero in range(1, 6):
    print(f"Contagem: {numero}")

for asterisco in range(1, 6):
    print('*' * asterisco)
